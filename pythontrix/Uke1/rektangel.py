# a) Opprett to variabler, bredde og høyde. Bestem verdiene selv.
bredde = 10
høyde = 3

# b) Skriv ut variablene til terminalen.
print("Bredde er: ", bredde, "\nHøyde er: ", høyde)

# c) Lag en tredje variabel, areal. Verdien skal være bredde ganger høyde.
areal = bredde * høyde

# d) Skriv ut arealet til terminalen.
print ("Arealet blir: ", areal)


# Output:
# Bredde er:  10 
# Høyde er:  3
# Arealet blir:  30